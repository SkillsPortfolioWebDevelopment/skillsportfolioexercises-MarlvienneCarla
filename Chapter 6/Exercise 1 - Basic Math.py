import math

# Ceil of a
a_ceil = 2.3
ceil_result = math.ceil(a_ceil)
print(f"The ceil of {a_ceil} is: {ceil_result}")

# Floor of a
a_floor = 2.3
floor_result = math.floor(a_floor)
print(f"The floor of {a_floor} is: {floor_result}")

# Factorial of a
a_factorial = 5
factorial_result = math.factorial(a_factorial)
print(f"The factorial of {a_factorial} is: {factorial_result}")

# Value of 23!
value_of_23 = math.factorial(23)
print(f"The value of 23! is: {value_of_23}")

# Square root of a
a_sqrt = 16
sqrt_result = math.sqrt(a_sqrt)
print(f"The square root of {a_sqrt} is: {sqrt_result}")
